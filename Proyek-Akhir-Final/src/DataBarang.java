    public void loadData(){
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        
        try {
            //Connection c = koneksi.getKoneksi();
            //Statement s = c.createStatement();
            //koneksi.getKoneksi();
            stat = koneksi.koneksi.createStatement();
            
            String sql = "SELECT * FROM data_motor";
            rs = stat.executeQuery(sql);
            
            while (rs.next()) {
                Object[] o = new Object[7];
                o [0] = rs.getString("id_motor");
                o [1] = rs.getString("merk_motor");
                o [2] = rs.getString("jenis_motor");
                o [3] = rs.getString("model_motor");
                o [4] = rs.getString("tahun_beli");
                o [5] = rs.getString("jarak_tempuh");
                o [6] = rs.getString("harga_jual");
                
                model.addRow(o);
            }
            rs.close();
            stat.close();
        } catch (Exception e) {
            System.out.println("terjadi kesalahan");
        }
    }
    
    public void cariData(){
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("ID Motor");
        tabel.addColumn("Merk Motor");
        tabel.addColumn("Jenis Motor");
        tabel.addColumn("Model Motor");
        tabel.addColumn("Tahun");
        tabel.addColumn("Jarak Tempuh");
        tabel.addColumn("Harga");
        
        try {
            Connection c = koneksi.getKoneksi();
            String sql = "Select * from data_motor where id_motor like '%" + jTextField1.getText() + "%'" +
                    "or model_motor like '%" + jTextField1.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {                
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                });
            }
            jTable1.setModel(tabel);
            loadData();
        } catch (Exception e) {
            System.out.println("Cari Data Error");
        }finally{
        }
    }
    
